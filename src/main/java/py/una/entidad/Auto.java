package py.una.entidad;

public class Auto {
	
	String chapa;
	String marca;
	
	
	public Auto() {
		super();
	}


	public Auto(String chapa, String marca) {
		super();
		this.chapa = chapa;
		this.marca = marca;
	}


	public String getChapa() {
		return chapa;
	}


	public void setChapa(String chapa) {
		this.chapa = chapa;
	}


	public String getMarca() {
		return marca;
	}


	public void setMarca(String marca) {
		this.marca = marca;
	}
	
	
	
	

}
